    private void jButtonCrearClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearClienteActionPerformed
        String devuelve;
        int cod=0;
        ArrayList<Cliente> clientes=new ArrayList();
    
        String nombre=jTextFieldNombre.getText();
        String tel=jTextFieldTelefono.getText();
            
            Matcher mTel;
            mTel=pTel.matcher(tel);
            if(mTel.matches()){
                if(clientes.isEmpty()){
                    cod=BBDD.generaCodCliente();
                }else{
                    cod++;
                }
                Cliente c=new Cliente(cod,nombre,tel);
                clientes.add(c);
                devuelve=BBDD.insertaClientes(clientes);
            }else{
                devuelve="El formato del Telefono no es v√°lido, vuelva a introducirlo correctamente";
            }
        jLabel4Comentarios.setText(devuelve);
    }//GEN-LAST:event_jButtonCrearClienteActionPerformed

