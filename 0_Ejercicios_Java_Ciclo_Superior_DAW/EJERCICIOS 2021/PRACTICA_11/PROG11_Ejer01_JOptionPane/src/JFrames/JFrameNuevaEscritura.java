    private void jButtonCrearEscrituraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearEscrituraActionPerformed
        String devuelve;
        int cod=0;
        ArrayList<Escritura> escrituras=new ArrayList();   
        
        String tipo=jTextFieldTipo.getText(); 
        String nomFicha=jTextFieldNombreFicha.getText();
        
        Matcher mFicha;
        mFicha=pFicha.matcher(nomFicha);
        if(mFicha.matches()){
            if(escrituras.isEmpty()){
                cod=BBDD.generaCodEscritura();
            }else{
                cod++;
            }
            Escritura e=new Escritura(cod,tipo,nomFicha);
            escrituras.add(e);
            devuelve=BBDD.insertaEscrituras(escrituras);
        }else{
            devuelve="El formato de la Ficha no es v√°lido, vuelva a introducirlo correctamente";
        }        
        jLabel4Comentarios.setText(devuelve);
    }//GEN-LAST:event_jButtonCrearEscrituraActionPerformed

